public class Main {
    public static void main(String[] args)
    {
        int num1 = 15;
        int num2 = 22;

        if(num1 % 2 ==0)
        {
            System.out.println(num1 + " is even");
        }
        else
        {
            System.out.println(num1 + " is odd");
        }
        if(num2 % 2 ==0)
        {
            System.out.println(num2 + " is even");
        }
        else
        {
            System.out.println(num2 + " is odd");
        }
    }
}